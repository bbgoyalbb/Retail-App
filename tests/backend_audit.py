# ──────────────────────────────────────────────────────────────────────────────

def print_report(a: Audit) -> int:
    sections: dict[str, list[Result]] = {}
    for r in a.results:
        sections.setdefault(r.section, []).append(r)

    print(f"\n{BOLD}{'═'*60}{RESET}")
    print(f"{BOLD}  BACKEND AUDIT REPORT{RESET}")
    print(f"{BOLD}{'═'*60}{RESET}")

    total_pass = total_fail = total_skip = 0
    for sec, results in sections.items():
        p = sum(1 for r in results if r.passed and not r.skipped)
        f = sum(1 for r in results if not r.passed and not r.skipped)
        sk = sum(1 for r in results if r.skipped)
        total_pass += p; total_fail += f; total_skip += sk
        status = f"{GREEN}✓{RESET}" if f == 0 else f"{RED}✗{RESET}"
        print(f"  {status} {BOLD}{sec.upper()}{RESET}: {p} passed, {f} failed{f', {sk} skipped' if sk else ''}")

    print(f"{BOLD}{'─'*60}{RESET}")
    overall = f"{GREEN}ALL PASSED{RESET}" if total_fail == 0 else f"{RED}{total_fail} FAILED{RESET}"
    print(f"  {BOLD}Total: {total_pass} passed  {total_fail} failed  {total_skip} skipped{RESET}  →  {overall}")
    print(f"{BOLD}{'═'*60}{RESET}\n")

    if total_fail > 0:
        print(f"{BOLD}{RED}Failed checks:{RESET}")
        for r in a.results:
            if not r.passed and not r.skipped:
                print(f"  {RED}✗{RESET} [{r.section}] {r.name}")
                if r.detail:
                    print(f"      {DIM}{r.detail}{RESET}")
        print()

    return 0 if total_fail == 0 else 1


