def audit_business_logic(a: Audit) -> None:
    a.section("3. BUSINESS LOGIC")

    today_str = date.today().isoformat()
    # Use a far-past date for the bill so the auto-assigned ref never collides with real data
    test_date_str = "2001-01-01"

    if not a.test_ref:
        a.log("logic", "All business logic tests", False, "no test_ref — bill creation failed, skipping", skipped=True)
        return

    # --- Balance after creation ---
    s, balances = a.req(f"/settlements/balances?ref={urllib.parse.quote(a.test_ref)}")
    if s != 200:
        a.log("logic", "Fetch initial balances", False, str(balances))
        return

    # By design: balances endpoint only returns amounts for items NOT yet settled (pay_mode not starting 'Settled').
    # Any payment marks the section Settled immediately — pending stores the actual difference (can be negative).
    # So we verify balance BEFORE payment, and verify item-level fields AFTER payment.
    got_fabric = round(float(balances.get("fabric", 0)), 2)
    got_advance_before = round(float(balances.get("advance", 0)), 2)
    a.log("logic", f"Initial fabric balance reported (items unsettled)", s == 200, f"got ₹{got_fabric}")

    # --- Partial settlement: pay ₹500 against fabric ---
    # By design: ANY payment marks items as Settled; fabric_pending on each item stores the actual remainder.
    settle_payload = {
        "customer_name": a.test_customer,
        "ref": a.test_ref,
        "payment_date": test_date_str,
        "payment_modes": ["Cash"],
        "fresh_payment": 500,
        "use_advance": False,
        "allot_fabric": 500,
        "allot_tailoring": 0,
        "allot_embroidery": 0,
        "allot_addon": 0,
        "allot_advance": 0,
    }
    s, b = a.req("/settlements/pay", "POST", settle_payload)
    a.log("logic", "POST /settlements/pay ₹500 → success", s == 200, str(b)[:80])

    # Verify item-level: fabric_pay_mode = 'Settled - Cash', fabric_received = 500, fabric_pending = 770
    # (items1=1000, item2=270 → total=1270; paid 500 pro-rata)
    if a.created_item_ids:
        s2, item_after = a.req(f"/items/{a.created_item_ids[0]}")
        pay_mode = item_after.get("fabric_pay_mode", "") if s2 == 200 else ""
        a.log("logic", "After ₹500 payment: fabric_pay_mode = 'Settled - Cash'",
              s2 == 200 and pay_mode.startswith("Settled"), f"got '{pay_mode}'")
        fabric_pending_sum = sum(
            round(float(item_after.get("fabric_pending", 0)), 2)
            for item_after in [item_after]
        )
        a.log("logic", "After ₹500 payment: fabric_pending on item is positive (partial)",
              s2 == 200 and item_after.get("fabric_pending", 0) > 0,
              f"fabric_pending={item_after.get('fabric_pending')} fabric_received={item_after.get('fabric_received')}")

    # --- Shortfall rejected ---
    bad_payload = {**settle_payload, "fresh_payment": 100, "allot_fabric": 500}
    s, b = a.req("/settlements/pay", "POST", bad_payload)
    a.log("logic", "Settlement shortfall (pay ₹100, allot ₹500) → 400", s == 400, str(b)[:80])

    # --- Zero allocation rejected ---
    zero_payload = {**settle_payload, "fresh_payment": 0, "allot_fabric": 0}
    s, b = a.req("/settlements/pay", "POST", zero_payload)
    a.log("logic", "Settlement with zero allocation → 400", s == 400, str(b)[:80])

    # --- Advance workflow ---
    adv_amount = 300.0
    adv_payload = {"ref": a.test_ref, "name": a.test_customer, "amount": adv_amount, "date": test_date_str, "mode": "Cash"}
    s, adv_b = a.req("/advances", "POST", adv_payload)
    a.log("logic", f"Create ₹{adv_amount} advance for ref", s == 200, str(adv_b)[:60])
    if s == 200 and "id" in adv_b:
        a.created_advance_ids.append(adv_b["id"])

    s, bal2 = a.req(f"/settlements/balances?ref={urllib.parse.quote(a.test_ref)}")
    adv_bal = round(float(bal2.get("advance", 0)), 2)
    expected_adv = round(got_advance_before + adv_amount, 2)
    a.log("logic", f"Advance balance increased by ₹{adv_amount}",
          abs(adv_bal - expected_adv) < 1.0,
          f"before=₹{got_advance_before} after=₹{adv_bal} expected=₹{expected_adv}")

    # --- Second fresh payment on same ref (items already Settled; additional payment reduces pending further) ---
    second_payload = {
        "customer_name": a.test_customer,
        "ref": a.test_ref,
        "payment_date": test_date_str,
        "payment_modes": ["Cash"],
        "fresh_payment": 200,
        "use_advance": False,
        "allot_fabric": 200,
        "allot_tailoring": 0,
        "allot_embroidery": 0,
        "allot_addon": 0,
        "allot_advance": 0,
    }
    s, b = a.req("/settlements/pay", "POST", second_payload)
    a.log("logic", "Second fresh payment on already-Settled ref → success", s == 200, str(b)[:80])

    # --- Tally / untally ---
    s, b = a.req("/daybook/tally", "POST", {
        "entry_ids": [a.test_ref], "date": test_date_str, "category": "fabric", "action": "tally"
    })
    a.log("logic", "POST /daybook/tally fabric → tallied", s == 200, str(b)[:60])

    s, b = a.req("/daybook/tally", "POST", {
        "entry_ids": [a.test_ref], "date": test_date_str, "category": "fabric", "action": "untally"
    })
    a.log("logic", "POST /daybook/tally untally fabric → ok", s == 200, str(b)[:60])

    # --- Bill with missing items → 400 ---
    bad_bill = {
        "customer_name": "X", "date": today_str, "payment_date": today_str,
        "items": [], "payment_modes": ["Cash"], "amount_paid": 0,
        "is_settled": False, "needs_tailoring": False,
    }
    s, b = a.req("/bills", "POST", bad_bill)
    a.log("logic", "POST /bills with empty items → 400", s == 400, str(b)[:80])

    # --- Reports: summary math check ---
    s, rev = a.req("/reports/summary")
    has_keys = s == 200 and "total_fabric_received" in rev and "total_fabric_pending" in rev
    a.log("logic", "GET /reports/summary has received + pending breakdown", has_keys,
          str(rev)[:80] if not has_keys else f"fabric_received=₹{rev.get('total_fabric_received')} fabric_pending=₹{rev.get('total_fabric_pending')}")

    # --- Overdue articles (delivery_date passed, not delivered) ---
    s, b = a.req("/dashboard")
    overdue = b.get("overdue_orders_count") if s == 200 else None
    a.log("logic", "Dashboard reports overdue_orders_count", s == 200 and overdue is not None,
          f"overdue_orders_count={overdue}" if overdue is not None else "key missing in: " + str(list(b.keys()))[:80])

    # --- Item update ---
    if a.created_item_ids:
        s, b = a.req(f"/items/{a.created_item_ids[0]}", "PUT", {"tailoring_status": "Pending"})
        a.log("logic", "PUT /items/{id} tailoring_status → updated", s == 200, str(b)[:60])
        # Verify update persisted
        s2, b2 = a.req(f"/items/{a.created_item_ids[0]}")
        a.log("logic", "Item update persisted in DB", s2 == 200 and b2.get("tailoring_status") == "Pending",
              f"got {b2.get('tailoring_status')}")


