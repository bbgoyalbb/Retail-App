# Self-signed SSL cert generated by build_and_run.bat — skip verification
_SSL_CTX = ssl.create_default_context()
_SSL_CTX.check_hostname = False
_SSL_CTX.verify_mode = ssl.CERT_NONE


# ──────────────────────────────────────────────────────────────────────────────
# Infrastructure
# ──────────────────────────────────────────────────────────────────────────────

RESET  = "\033[0m"
GREEN  = "\033[32m"
RED    = "\033[31m"
YELLOW = "\033[33m"
BOLD   = "\033[1m"
CYAN   = "\033[36m"
DIM    = "\033[2m"


@dataclass
class Result:
    section: str
    name: str
    passed: bool
    detail: str = ""
    skipped: bool = False


@dataclass
class Audit:
    base_url: str
    token: str | None = None
    manager_token: str | None = None
    results: list[Result] = field(default_factory=list)

    # Test data refs for cleanup
    created_item_ids: list[str] = field(default_factory=list)
    created_advance_ids: list[str] = field(default_factory=list)
    created_usernames: list[str] = field(default_factory=list)
    test_ref: str | None = None
    test_customer: str = field(default_factory=lambda: f"__AuditBot_{int(time.time())}")  # unique per run

    def req(
        self,
        path: str,
        method: str = "GET",
        data: dict | None = None,
        token: str | None = "__default__",
        files: bytes | None = None,
        content_type: str | None = None,
    ) -> tuple[int, Any]:
        if token == "__default__":
            token = self.token
        headers: dict[str, str] = {}
        body: bytes | None = None
        if data is not None and files is None:
            body = json.dumps(data).encode()
            headers["Content-Type"] = "application/json"
        if content_type:
            headers["Content-Type"] = content_type
        if files is not None:
            body = files
        if token:
            headers["Authorization"] = f"Bearer {token}"
        url = f"{self.base_url}{path}"
        req = urllib.request.Request(url, data=body, headers=headers, method=method)
        try:
            with urllib.request.urlopen(req, timeout=30, context=_SSL_CTX) as resp:
                raw = resp.read()
                ct = resp.headers.get("Content-Type", "")
                if "json" in ct:
                    return resp.status, json.loads(raw.decode())
                return resp.status, raw.decode(errors="replace")
        except urllib.error.HTTPError as exc:
            payload = exc.read().decode(errors="replace")
            try:
                return exc.code, json.loads(payload)
            except Exception:
                return exc.code, payload

    def log(self, section: str, name: str, passed: bool, detail: str = "", skipped: bool = False) -> None:
        self.results.append(Result(section, name, passed, detail, skipped))
        if skipped:
            icon = f"{YELLOW}SKIP{RESET}"
        elif passed:
            icon = f"{GREEN}PASS{RESET}"
        else:
            icon = f"{RED}FAIL{RESET}"
        line = f"  [{icon}] {name}"
        if detail:
            short = detail if len(detail) <= 120 else detail[:117] + "…"
            line += f"  {DIM}{short}{RESET}"
        print(line)

    def section(self, title: str) -> None:
        print(f"\n{BOLD}{CYAN}── {title}{RESET}")


