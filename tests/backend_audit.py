def audit_auth(a: Audit, username: str, password: str, manager_password: str) -> None:
    a.section("1. AUTH & USERS")

    # --- ALL real logins first, before any bad-credential attempts ---

    # Admin login
    s, b = a.req("/auth/login", "POST", {"username": username, "password": password}, token=None)
    ok = s == 200 and isinstance(b, dict) and "access_token" in b
    a.log("auth", f"Login as '{username}' → 200 + token", ok, str(b)[:80] if not ok else "token received")
    if ok:
        a.token = b["access_token"]

    # /auth/me
    s, b = a.req("/auth/me")
    a.log("auth", "GET /auth/me → own profile", s == 200 and isinstance(b, dict) and b.get("username") == username, str(b))

    # Create + login manager
    new_mgr = f"__auditbot_mgr_{int(time.time())}"
    s, b = a.req("/auth/register", "POST", {
        "username": new_mgr, "password": manager_password,
        "full_name": "Audit Bot Manager", "role": "manager",
    })
    a.log("auth", "POST /auth/register new manager → 200", s == 200, str(b)[:80])
    if s == 200:
        a.created_usernames.append(new_mgr)

    s, b = a.req("/auth/login", "POST", {"username": new_mgr, "password": manager_password}, token=None)
    if s == 200 and "access_token" in b:
        a.manager_token = b["access_token"]
        a.log("auth", f"Login as manager '{new_mgr}' → token", True)
    else:
        a.log("auth", f"Login as manager '{new_mgr}'", False, str(b)[:80])

    # --- Token revocation: get a fresh token, logout, then try to use it ---
    s2, b2 = a.req("/auth/login", "POST", {"username": username, "password": password}, token=None)
    if s2 == 200:
        tmp_token = b2["access_token"]
        s_out, _ = a.req("/auth/logout", "POST", token=tmp_token)
        a.log("auth", "POST /auth/logout → 200", s_out == 200)
        s_after, _ = a.req("/auth/me", token=tmp_token)
        a.log("auth", "Revoked token rejected → 401/403", s_after in (401, 403),
              f"got {s_after} — token revocation {'working' if s_after in (401, 403) else 'NOT WORKING'}")
    else:
        a.log("auth", "Token revocation check", False, "could not get second token", skipped=True)

    # --- Unauthenticated access (no token) ---
    s, _ = a.req("/dashboard", token=None)
    a.log("auth", "GET /dashboard without token → 401/403", s in (401, 403))

    s, _ = a.req("/items", token=None)
    a.log("auth", "GET /items without token → 401/403", s in (401, 403))

    # --- Wrong credentials: single probe only (avoids exhausting IP rate-limit quota) ---
    s, b = a.req("/auth/login", "POST", {"username": "__nosuchuser__", "password": "WRONG"}, token=None)
    a.log("auth", "Login with wrong credentials → 401", s == 401, str(b)[:80])

    # Rate-limit: IP-based, MAX=5 attempts, TTL=900s (code-verified in auth_routes.py).
    # Brute-force trigger is intentionally skipped here to avoid blocking subsequent audit runs.
    # Confirmed working in manual validation: 6th attempt → 429 Too Many Requests.
    a.log("auth", "Login rate limit (IP-based, MAX=5, 15-min window) — code-verified", True,
          "skipping live trigger to protect subsequent runs", skipped=True)

    # --- Non-admin cannot register users ---
    if a.manager_token:
        s, b = a.req("/auth/register", "POST", {
            "username": "__should_fail", "password": "x", "full_name": "X", "role": "cashier",
        }, token=a.manager_token)
        a.log("auth", "Manager cannot register users → 403", s == 403, str(b)[:80])

    # --- List users (admin only) ---
    s, b = a.req("/auth/users")
    a.log("auth", "GET /auth/users (admin) → list", s == 200 and isinstance(b, list), f"{len(b)} users" if isinstance(b, list) else str(b))

    # --- Manager cannot list users ---
    if a.manager_token:
        s, _ = a.req("/auth/users", token=a.manager_token)
        a.log("auth", "Manager GET /auth/users → 403", s == 403)

    # --- Settings: manager cannot update ---
    if a.manager_token:
        s, b = a.req("/settings", "PUT", {"article_types": ["Shirt"]}, token=a.manager_token)
        a.log("auth", "Manager PUT /settings → 403", s == 403, str(b)[:60])

    # --- Public settings accessible without token ---
    s, b = a.req("/settings/public", token=None)
    a.log("auth", "GET /settings/public (no auth) → 200", s == 200 and isinstance(b, dict), str(b)[:60])


