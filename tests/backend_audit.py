# SECTION 4 — SECURITY HARDENING
# ──────────────────────────────────────────────────────────────────────────────

def audit_security(a: Audit) -> None:
    a.section("4. SECURITY")

    # Build a list of write endpoints and verify they all require auth
    protected_writes = [
        ("POST",   "/bills",                     {"customer_name": "x", "date": "2026-01-01", "payment_date": "2026-01-01", "items": [{"barcode": "x", "qty": 1, "price": 1, "discount": 0}], "payment_modes": ["Cash"], "amount_paid": 0, "is_settled": False, "needs_tailoring": False}),
        ("POST",   "/advances",                  {"ref": "x", "name": "x", "amount": 1, "date": "2026-01-01", "mode": "Cash"}),
        ("POST",   "/daybook/tally",             {"entry_ids": ["x"], "date": "2026-01-01", "category": "fabric", "action": "tally"}),
        ("POST",   "/settlements/pay",           {"customer_name": "x", "ref": "x", "payment_date": "2026-01-01", "payment_modes": ["Cash"], "fresh_payment": 0, "use_advance": False, "allot_fabric": 0, "allot_tailoring": 0, "allot_embroidery": 0, "allot_addon": 0, "allot_advance": 0}),
        ("PUT",    "/settings",                  {"article_types": ["Shirt"]}),
        ("POST",   "/auth/register",             {"username": "x", "password": "x", "full_name": "x", "role": "manager"}),
        ("DELETE", "/auth/users/nonexistent",    None),
        ("GET",    "/export/excel",              None),
        ("GET",    "/backup",                    None),
    ]

    for method, path, payload in protected_writes:
        s, _ = a.req(path, method, payload, token=None)
        a.log("security", f"{method} {path} (no token) → 401/403", s in (401, 403), f"got {s}")

    # Admin-only endpoints blocked for manager
    if a.manager_token:
        admin_only = [
            ("PUT",    "/settings",                {"article_types": ["Shirt"]}),
            ("POST",   "/auth/register",           {"username": "x2", "password": "x", "full_name": "x", "role": "cashier"}),
            ("GET",    "/auth/users",              None),
            ("GET",    "/export/excel",            None),
            ("GET",    "/backup",                  None),
            ("GET",    "/db/audit",                None),
        ]
        for method, path, payload in admin_only:
            s, _ = a.req(path, method, payload, token=a.manager_token)
            if path == "/db/audit" and s != 403:
                a.log("security", f"Manager {method} {path} → 403  ⚠️ REAL FINDING: /db/audit missing role guard", s == 403, f"got {s} — any authenticated user can access the DB audit endpoint")
            else:
                a.log("security", f"Manager {method} {path} → 403", s == 403, f"got {s}")
    else:
        a.log("security", "Manager role-guard checks", False, "manager token unavailable", skipped=True)

    # CORS: public settings no auth needed
    s, b = a.req("/settings/public", token=None)
    a.log("security", "GET /settings/public accessible without auth", s == 200)

    # Malformed token rejected
    s, _ = a.req("/dashboard", token="not.a.real.jwt.token")
    a.log("security", "Malformed JWT → 401/403/422", s in (401, 403, 422), f"got {s}")

    # SQL/NoSQL injection attempt in query params
    s, b = a.req("/search?q=%24where%3D1%3D1&limit=5")
    a.log("security", "NoSQL injection in search param handled safely (no 500)", s != 500, f"got {s}")

    s, b = a.req("/items?name=%7B%22%24gt%22%3A%22%22%7D&limit=5")
    a.log("security", "NoSQL injection in items?name handled safely (no 500)", s != 500, f"got {s}")


# ──────────────────────────────────────────────────────────────────────────────
# SECTION 5 — CLEANUP
# ──────────────────────────────────────────────────────────────────────────────

def cleanup(a: Audit) -> None:
    a.section("5. CLEANUP")
    deleted = 0
    for item_id in a.created_item_ids:
        s, _ = a.req(f"/items/{item_id}", "DELETE")
        if s == 200:
            deleted += 1
    a.log("cleanup", f"Deleted {deleted}/{len(a.created_item_ids)} test items", deleted == len(a.created_item_ids))

    adv_deleted = 0
    for adv_id in a.created_advance_ids:
        s, _ = a.req(f"/advances/{adv_id}", "DELETE")
        if s == 200:
            adv_deleted += 1

    # Also sweep the ref for any auto-generated adjustment advances (e.g. negative entries
    # created internally by settlements/pay when use_advance=True) that aren't in created_advance_ids
    if a.test_ref:
        s_al, adv_list = a.req(f"/advances?ref={urllib.parse.quote(a.test_ref)}&limit=100")
        if s_al == 200:
            for adv in (adv_list if isinstance(adv_list, list) else adv_list.get("advances", [])):
                adv_id = adv.get("id")
                if adv_id and adv_id not in a.created_advance_ids:
                    s_d, _ = a.req(f"/advances/{adv_id}", "DELETE")
                    if s_d == 200:
                        adv_deleted += 1

    if a.created_advance_ids:
        a.log("cleanup", f"Deleted {adv_deleted}/{len(a.created_advance_ids)} test advances", adv_deleted >= len(a.created_advance_ids))

    user_deleted = 0
    for uname in a.created_usernames:
        s, _ = a.req(f"/auth/users/{uname}", "DELETE")
        if s == 200:
            user_deleted += 1
    if a.created_usernames:
        a.log("cleanup", f"Deleted {user_deleted}/{len(a.created_usernames)} test users", user_deleted == len(a.created_usernames))


